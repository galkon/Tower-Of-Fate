package gameworld;

/**
 * Special types of item that can be dropped by players and sold by a vendor
 * to help players with certain features of the game
 * 
 * @author Damian Kaye
 *
 */

public abstract class VendorItem extends Item{

	
	
	public VendorItem(String name, String description) {
		
		super(name, description, false);
		
		
	}
	
	public abstract int getGoldPrice();

}
